package xyz.zzyymaggie.mybatis.model;

public class Style {
    private long styleColorId;
    private int year;
    private int month;
    
    public long getStyleColorId() {
        return styleColorId;
    }
    public void setStyleColorId(long styleColorId) {
        this.styleColorId = styleColorId;
    }
    public int getYear() {
        return year;
    }
    public void setYear(int year) {
        this.year = year;
    }
    public int getMonth() {
        return month;
    }
    public void setMonth(int month) {
        this.month = month;
    }
}
